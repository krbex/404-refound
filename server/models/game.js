const mongoose = require('mongoose');

const { Schema } = mongoose;

let gameSchema = new Schema({
    name: {
        type: String,
        required: true,
        trim: true,
    },
    description: {
        type: String,
        required: true,
        trim: true,
    },
    image: {
        type: String,
        required: true,
        trim: true,
    },
    price: {
        type: Number,
        required: true,
        trim: true,
    },
});

const Game = mongoose.model('Game', gameSchema);

module.exports = Game;